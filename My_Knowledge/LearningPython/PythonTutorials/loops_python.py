a = ["banana", "apple", "microsoft"]
print(a)

# For Loops

for element in a:
	print(element)

b = [20, 10, 5]

total = 0
for e in b:
	total += e
print(total)

## For loop with range

c = list(range(1,5))
print(c)

total2 = 0

for i in range(1,5):
	total2 += i
print(total2)

total3 = 0
for i in range(1, 9):
	total3 += i
print(total3)

print(list(range(1, 100)))


