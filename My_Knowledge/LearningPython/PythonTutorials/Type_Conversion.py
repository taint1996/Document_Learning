########## String to Int
str = "100"
x = int(str)
y = x + 200
print(y)

a = "100"
b = "-50"
c = int(a) + int(b)
print(c)

########## String to Float
x = "100"
y = "-50.5"
z = float(x) + float(y)
print(z)

########## Convert to Tuples and Lists
lst = [1, 2, 3, 4, 5]
print(lst)
tpl = tuple(lst)
print(tpl)
